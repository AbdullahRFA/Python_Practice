# This Python code snippet demonstrates the usage of the `time` class from the `datetime` module. Here's a breakdown of what the code does:
from datetime import time

# Create a specific time
specific_time = time(14, 30, 15)  # 14:30:15
print("Specific time:", specific_time)

# Access hour, minute, second, and microsecond
print("Hour:", specific_time.hour)
print("Minute:", specific_time.minute)
print("Second:", specific_time.second)
